#include "../../inc/libft_inc/libft.h"

void	ft_lstadd_back(t_voidlist **lst, t_voidlist *new)
{
	t_voidlist	*last;

	if (!new || !lst)
		return ;
	if (*lst == NULL)
	{
		*lst = new;
		return ;
	}
	last = ft_lstlast(*lst);
	last->next = new;
}
